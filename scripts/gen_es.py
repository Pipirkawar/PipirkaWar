COMBO_RE = re.compile(r"^(.+?)\s+\{user\}\s+was\s+(.+?),\s+(.+?)\s+and\s+(.+)$")
SUDDENLY_RE = re.compile(r"^(.+?)\s+\{user\}\s+was\s+(.+?)\.\s+Suddenly\s+(.+?)\s+and\s+(.+)$")


def translate_forest_combo(text: str) -> str | None:
    """Translate a combinatorial forest log via building blocks."""
    m = COMBO_RE.match(text)
    suddenly = False
    if not m:
        m = SUDDENLY_RE.match(text)
        suddenly = True
    if not m:
        return None

    emoji = m.group(1)
    action_en = m.group(2)
    trans_raw = m.group(3)
    result_en = m.group(4)

    trans_clean = trans_raw.rstrip(" —")
    has_dash = trans_raw.endswith("—") or trans_raw.rstrip().endswith("—")

    a = ACTION_ES.get(action_en)
    t = TRANSITION_ES.get(trans_clean)
    r = RESULT_ES.get(result_en)

    if a is None or t is None or r is None:
        return None

    if suddenly:
        if has_dash:
            return f"{emoji} {{user}} estaba {a}. De repente {t} — ¡y {r}"
        return f"{emoji} {{user}} estaba {a}. De repente {t} y {r}"
    if has_dash:
        return f"{emoji} {{user}} estaba {a}, {t} — ¡y {r}"
    return f"{emoji} {{user}} estaba {a}, {t} y {r}"


