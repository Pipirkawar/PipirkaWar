def apply_subs(text: str, subs: list[tuple[str, str]]) -> str:
    """Apply substitution list to text."""
    t = text
    for en, es in subs:
        t = t.replace(en, es)
    return t


def translate_text(text: str, file_type: str) -> str:  # noqa: PLR0911
    """Main translation dispatcher. Checks direct lookup first, then patterns."""
    # Direct translation lookup — single merged dict, O(1)
    if text in ALL_DIRECT:
        return ALL_DIRECT[text]

    if file_type == "forest":
        combo = translate_forest_combo(text)
        if combo:
            return combo
        return apply_subs(text, FOREST_UNIQUE_SUBS)

    if file_type == "duel":
        return apply_subs(text, PHRASES)

    if file_type == "clan":
        return apply_subs(text, CLAN_SUBS)

    if file_type == "oracle":
        return apply_subs(text, ORACLE_SUBS)

    return text


