def convert_file(en_name: str, es_name: str, file_type: str) -> None:
    with open(TEMPLATES_DIR / en_name, encoding="utf-8") as f:
        data = json.load(f)

    es_templates = []
    untranslated = 0
    for t in data["templates"]:
        es_t = dict(t)
        es_t["id"] = t["id"].replace(".en.", ".es.")
        translated = translate_text(t["text"], file_type)
        if translated == t["text"]:
            untranslated += 1
        es_t["text"] = translated
        es_templates.append(es_t)

    es_data = {"version": data["version"], "templates": es_templates}
    with open(TEMPLATES_DIR / es_name, "w", encoding="utf-8") as f:
        json.dump(es_data, f, ensure_ascii=False, indent=2)
        f.write("\n")

    total = len(es_templates)
    pct = (total - untranslated) / total * 100
    print(f"  ✓ {es_name}: {total} templates ({pct:.0f}% translated, {untranslated} remaining)")


def main() -> None:
    print("Generating Spanish templates…")
