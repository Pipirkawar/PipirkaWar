"""VO домена «Рейд-боссы» (Спринт 3.3-A, ГДД §10 «Рейд-боссы»).

Каркас доменных VO без бизнес-логики раунд-механики и use-case-ов
(они приходят в Спринтах 3.3-B и 3.3-C соответственно).

Ключевые VO:

- **`BossKind`** — тип босса. На старте Спринта 3.3 — только `RAID`
    (босс-человек, случайный из топ-30 игроков по длине, вызываемый
    игроком уровня 9+). Зарезервировано для будущих типов
    (мировой босс, событийный босс) — добавим в схему `bosses:`
    `balance.yaml`-а отдельным расширением, когда они появятся.
- **`BossFightStatus`** — состояния рейд-боя: `LOBBY` (20 мин сбор
    рейдеров, ГДД §10.3), `IN_BATTLE` (раунды 20 сек–1 мин, ГДД §10.4),
    `FINISHED` (исход применён — рейдеры победили или босс победил,
    ГДД §10.5), `CANCELLED` (никто не пришёл / админ-вмешательство /
    клан-получатель заморозился посреди лобби).
- **`BossDamage`** — VO «урон в раунде»: целое число см > 0. Применяется
    как уменьшение `current_boss_length_cm` (когда рейдер блокирует
    атаку и забирает у босса) или как уменьшение длины рейдера (когда
    босс пробил блок). Точная боевая логика — Спринт 3.3-C
    (`boss_round_resolution`).
"""

from __future__ import annotations

import enum
from dataclasses import dataclass


class BossKind(str, enum.Enum):
    """Тип рейд-босса (ГДД §10).

    На старте Спринта 3.3 — только `RAID` (босс — случайный игрок
    из топ-30 по длине, вызывается игроком уровня 9+). Зарезервировано
    для будущих типов (`WORLD_BOSS`, `EVENT_BOSS` и т. п.); добавятся
    отдельной расширенной секцией в `bosses:` `balance.yaml`-а.
    """

    RAID = "raid"


class BossFightStatus(str, enum.Enum):
    """Жизненный цикл рейд-боя (ГДД §10.3 / §10.4 / §10.5).

    `LOBBY` — после `/boss`-вызова: саммонер кинул вызов, в чате клана
    саммонера объявление, идёт сбор рейдеров (20 мин, ГДД §10.3).
    Глобальный кулдаун 4ч стартует с момента входа в `LOBBY`. На переходе
    `LOBBY → IN_BATTLE` проверяются минимальные условия (ГДД §10.3:
    «минимум 1 игрок»; де-факто всегда выполняется, потому что саммонер —
    обязательный участник как рейдер).

    `IN_BATTLE` — раунды 20 сек – 1 мин (ГДД §10.4). Босс — 3 атаки
    (бьёт всех), рейдеры — 3 блока (каждый один). Если босс попал
    не в блок → рейдер выбывает + теряет длину; если в блок → рейдер
    остаётся + забирает длину босса. Раунды повторяются до завершения.

    `FINISHED` — `FinishBossFight` (3.3-C) применил исход: либо
    `length(boss) < victory_threshold_cm` (рейдеры победили — делят
    длину босса + получают % от системы; per-player ролл скроллов,
    ГДД §10.5 + ГДД §2.8.5), либо все рейдеры выбыли (босс победил —
    забирает длину рейдеров + % от системы). Идемпотентность повторного
    финиша — на уровне use-case через `was_already_finished`.

    `CANCELLED` — рейд закрыт без боя. Возможные причины:
    — никто из top-30 рейдеров не выжил после первого раунда (теоретически);
    — саммонер вышел из лобби до 20 мин и других рейдеров не было;
    — клан саммонера заморозился посреди лобби;
    — миграция group → supergroup сменила chat_id посреди лобби;
    — админ-вмешательство.
    Глобальный кулдаун **сохраняется** (4ч начинается с попытки
    призыва, не с успешного завершения).
    """

    LOBBY = "lobby"
    IN_BATTLE = "in_battle"
    FINISHED = "finished"
    CANCELLED = "cancelled"


@dataclass(frozen=True, slots=True)
class BossDamage:
    """Урон в рейд-бою — целое число см > 0 (ГДД §10.4).

    Применяется домен-сервисом `boss_round_resolution` (3.3-C) в двух
    направлениях:

    1. **Босс → рейдер** (атака пробила блок). Рейдер теряет `cm`
       единиц длины и выбывает из боя.
    2. **Рейдер → босс** (атака заблокирована). Рейдер «забирает»
       `cm` единиц длины у босса (`current_boss_length_cm` уменьшается
       на эту величину).

    Конкретные числа — `BossesConfig.base_damage_cm` (Спринт 3.3-A).
    Логика выбора направления — `boss_round_resolution` (Спринт 3.3-C).
    """

    cm: int

    def __post_init__(self) -> None:
        if not isinstance(self.cm, int) or isinstance(self.cm, bool):
            raise TypeError(f"BossDamage.cm must be int, got {type(self.cm).__name__}")
        if self.cm <= 0:
            raise ValueError(f"BossDamage.cm must be > 0, got {self.cm}")


__all__ = [
    "BossDamage",
    "BossFightStatus",
    "BossKind",
]
