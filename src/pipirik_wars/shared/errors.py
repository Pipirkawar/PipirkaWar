"""Общие исключения, не привязанные к слою.

Каждый слой может расширять собственными типами (например,
`domain.player.errors.LengthBelowMinimumError`), но базовые типы здесь —
для консистентного маппинга на ответы пользователю и логи.
"""

from __future__ import annotations


class PipirikError(Exception):
    """Базовое исключение проекта. Никогда не бросается напрямую."""


class DomainError(PipirikError):
    """Нарушение бизнес-правил (например, попытка опуститься ниже 20 см).

    Бросается из `domain/`. На уровне bot/admin маппится на дружелюбное
    сообщение пользователю.
    """


class ConcurrencyError(PipirikError):
    """Конкурентное обновление (optimistic lock не сошёлся, дубль idempotency).

    Обычно ловится одной транзакцией выше; не должно доходить до пользователя.
    """


class IntegrityError(PipirikError):
    """Нарушение инварианта целостности данных.

    Например: запись в `audit_log` не дописалась, хотя бизнес-таблица
    обновилась. ГДД §0 — это критическая ошибка, всё откатывается.
    """


class ConfigError(PipirikError):
    """Ошибка конфигурации (невалидный `balance.yaml`, отсутствует обязательный env)."""
